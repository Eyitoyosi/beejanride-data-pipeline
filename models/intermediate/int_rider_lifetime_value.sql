/*
  int_rider_lifetime_value
  ────────────────────────
  Calculates rider lifetime value (LTV) and segmentation signals.
  LTV = total net revenue generated by the rider across all completed trips.

  View materialization is used here since this aggregates across the entire trip history per rider, 
  which is expensive to compute but queried frequently by downstream models.
*/

with trips as (
    select * from {{ ref('int_trips_enriched') }}
    where trip_status = 'completed'
),

rider_stats as (
    select
        rider_id,
        rider_signup_date,
        rider_country,
        referral_code,

        count(*)                          as total_trips,
        sum(actual_fare)                  as total_gross_spend,
        sum(net_revenue)                  as rider_ltv,
        avg(actual_fare)                  as avg_fare,
        min(trip_date)                    as first_trip_date,
        max(trip_date)                    as last_trip_date,

        countif(is_corporate)             as corporate_trips,
        countif(payment_method = 'card')  as card_trips,
        countif(payment_method = 'cash')  as cash_trips,

        -- Engagement recency
        date_diff(current_date(), max(trip_date), day) as days_since_last_trip,

        -- Cohort (signup month)
        format_date('%Y-%m', rider_signup_date)        as signup_cohort

    from trips
    group by
        rider_id, rider_signup_date, rider_country, referral_code
),

-- RFM-style segmentation
segmented as (
    select
        *,
        case
            when total_trips >= 20 and days_since_last_trip <= 30  then 'champion'
            when total_trips >= 10 and days_since_last_trip <= 60  then 'loyal'
            when days_since_last_trip <= 30                         then 'recent'
            when days_since_last_trip between 31 and 90            then 'at_risk'
            else 'churned'
        end as rider_segment,

        case when referral_code is not null then true else false end
            as is_referred

    from rider_stats
)

select * from segmented
